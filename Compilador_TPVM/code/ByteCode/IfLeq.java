package ByteCode;

public class IfLeq extends ConditionalJumps{
	
	/**
	 * Constructura de la clase IfLeq
	 * @param numero
	 */
	public IfLeq(int numero){
		
		super(numero);
	}
	

//--------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	/**
	 * Constructura de la clase IfLeq
	 */
	public IfLeq(){
		
		super();
	}
	

//--------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	public boolean execute(int n1, int n2){
		
		return (!(n1 <= n2));
	}


//--------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	public ByteCode parse(String nombre, int i){
		
		if(nombre.equals("IFLEQ"))
			return new IfLeq(i);
		
		else return null;
	}
	

//--------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	
	public String toString(){
		
		return "IfLeq " + this.parametro;
	}
	

//--------------------------------------------------------------------------------------------------------------------------------------------------------------
	
}
