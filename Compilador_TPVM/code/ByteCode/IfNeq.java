package ByteCode;

public class IfNeq extends ConditionalJumps{

	/**
	 * Constructura de la clase IfNeq
	 * @param numero
	 */
	public IfNeq(int numero){
		
		super(numero);
	}
	

//--------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	/**
	 * Constructura de la clase IfNeq
	 */
	public IfNeq(){
		
		super();
	}
	

//--------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	public boolean execute(int n1, int n2){
		
		return (!(n1 != n2));
	}


//--------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	public ByteCode parse(String nombre, int i){
		
		if(nombre.equals("IFNEQ"))
			return new IfNeq(i);
		
		else return null;
	}
	

//--------------------------------------------------------------------------------------------------------------------------------------------------------------

	public String toString(){
		
		return "IfNeq " + this.parametro;
	}
	

//--------------------------------------------------------------------------------------------------------------------------------------------------------------

}
