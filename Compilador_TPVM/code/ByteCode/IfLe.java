package ByteCode;

public class  IfLe extends ConditionalJumps {
	
	/**
	 * Constructura de la clase IfLe
	 * @param numero
	 */
	public IfLe(int numero){
		
		super(numero);
	}
	

//--------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	/**
	 * Constructura de la clase IfLe
	 */
	public IfLe(){
		
		super();
	}


//--------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	public boolean execute(int n1, int n2){
		
		return (!(n1 < n2));
	}


//--------------------------------------------------------------------------------------------------------------------------------------------------------------

	public ByteCode parse(String nombre, int i){
		
		if(nombre.equals("IFLE"))
			return new IfLe(i);
		
		else return null;
	}


//--------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	public String toString(){
		
		return "IfLe " + this.parametro;
	}



	

//--------------------------------------------------------------------------------------------------------------------------------------------------------------
	
}
