package ByteCode;

import CPU.CPU;

public class Halt extends ByteCode{

	
	public ByteCode parse(String[] s) {
		
		if (s.length == 1 && s[0].equals("HALT"))
			return new Halt();
		
		else
			return null;
	}

//--------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	public boolean execute(CPU cpu){
		
		return cpu.Halt();
	}

//--------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	public String toString(){
		
		return "Halt";
	}
	
//--------------------------------------------------------------------------------------------------------------------------------------------------------------	
	
}
