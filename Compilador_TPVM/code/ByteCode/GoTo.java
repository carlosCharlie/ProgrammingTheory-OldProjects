package ByteCode;

import CPU.CPU;

public class GoTo extends ByteCodeOneParameter{

	public GoTo(int numero){
		
		super(numero);
	}
	

//--------------------------------------------------------------------------------------------------------------------------------------------------------------

	GoTo(){
		
		super();
	}


//--------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	public ByteCode parse(String nombre, int i){
		
		if(nombre.equals("GOTO"))
			return new GoTo(i);
		
		else return null;
	}


//--------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	public boolean execute(CPU cpu){
		
		cpu.setProgramCounter(this.parametro);
		return true;
	}


//--------------------------------------------------------------------------------------------------------------------------------------------------------------
	
	public String toString(){
		
		return "GoTo " + this.parametro;
	}
	

//--------------------------------------------------------------------------------------------------------------------------------------------------------------
	
}
